/* =========================================================================
 * SECTION 1 – HD44780 LCD CONTROLLER
 *
 * The HD44780 is interfaced via two adjacent byte addresses.  The RS
 * (register-select) line distinguishes commands from data; on the DMP9
 * this is wired so that:
 *   0x490000  RS=0  → Instruction Register (write command / read busy flag)
 *   0x490001  RS=1  → Data Register        (write/read DDRAM or CGRAM)
 *
 * The DMP9/DMP16 display is a 4-line × 20-character LCD module.
 * HD44780 DDRAM layout for common 4-line, 20-char displays:
 *   Line 1: 0x00–0x13   (display column 0–19)
 *   Line 2: 0x40–0x53
 *   Line 3: 0x14–0x27   ← some 4-line modules use 0x14 offset for line 3
 *   Line 4: 0x54–0x67
 *
 * Verify the exact line-3/4 base by watching the firmware's Set-DDRAM
 * commands (opcodes 0x80–0xFF) during initialisation.
 * ========================================================================= */

/** LCD command register (RS=0): write instruction, read busy flag + AC */
#define LCD_CMD                 0x490000UL

/** LCD data register (RS=1): write/read DDRAM or CGRAM data */
#define LCD_DATA                0x490001UL


/* -------------------------------------------------------------------------
 * HD44780 Instruction Set
 * All values are written to LCD_CMD (RS=0).
 * Execution times assume fosc ~270 kHz (typical 5V HD44780).
 * -------------------------------------------------------------------------- */

/* --- Housekeeping commands ------------------------------------------------ */
#define LCD_CMD_CLEAR_DISPLAY   0x01  /**< Clear all DDRAM, reset AC to 0.   ~1.52 ms */
#define LCD_CMD_RETURN_HOME     0x02  /**< Reset AC to 0, return shifted display. ~1.52 ms */

/* --- Entry Mode Set (0x04–0x07): set cursor move direction + display shift -- */
/** @defgroup LCD_ENTRY Entry Mode Set (0x04 base) */
#define LCD_CMD_ENTRY_MODE_BASE 0x04  /**< Base opcode; OR the flags below */
#define LCD_ENTRY_INC           0x02  /**< Increment AC after each R/W (cursor right) */
#define LCD_ENTRY_DEC           0x00  /**< Decrement AC after each R/W (cursor left) */
#define LCD_ENTRY_SHIFT         0x01  /**< Shift display on write (INC→left; DEC→right) */
#define LCD_ENTRY_NOSHIFT       0x00  /**< No display shift */

/* Commonly used composite values */
#define LCD_CMD_ENTRY_INC_NOSHIFT   0x06  /**< Increment, no shift  (typical init) */
#define LCD_CMD_ENTRY_DEC_NOSHIFT   0x04  /**< Decrement, no shift */
#define LCD_CMD_ENTRY_INC_SHIFT     0x07  /**< Increment + shift display left */
#define LCD_CMD_ENTRY_DEC_SHIFT     0x05  /**< Decrement + shift display right */

/* --- Display ON/OFF Control (0x08–0x0F) ------------------------------------- */
/** @defgroup LCD_DISP Display ON/OFF Control (0x08 base) */
#define LCD_CMD_DISPLAY_BASE    0x08  /**< Base opcode; OR the flags below */
#define LCD_DISP_ON             0x04  /**< Display ON */
#define LCD_DISP_OFF            0x00  /**< Display OFF (contents preserved in DDRAM) */
#define LCD_CURSOR_ON           0x02  /**< Underline cursor visible */
#define LCD_CURSOR_OFF          0x00  /**< Cursor off */
#define LCD_BLINK_ON            0x01  /**< Blinking block cursor */
#define LCD_BLINK_OFF           0x00  /**< No blink */

/* Commonly used composite values */
#define LCD_CMD_DISPLAY_OFF         0x08  /**< Display off, cursor off, blink off */
#define LCD_CMD_DISPLAY_ON_NOCURSOR 0x0C  /**< Display on, cursor off */
#define LCD_CMD_DISPLAY_ON_CURSOR   0x0E  /**< Display on, cursor on (underline) */
#define LCD_CMD_DISPLAY_ON_BLINK    0x0F  /**< Display on, cursor on + blinking block */

/* --- Cursor / Display Shift (0x10–0x1F) ------------------------------------- */
/** @defgroup LCD_SHIFT Cursor / Display Shift (0x10 base) */
#define LCD_CMD_SHIFT_BASE      0x10  /**< Base opcode; OR the flags below */
#define LCD_SHIFT_DISPLAY       0x08  /**< 1=shift display, 0=move cursor */
#define LCD_SHIFT_CURSOR        0x00
#define LCD_SHIFT_RIGHT         0x04  /**< Shift/move right */
#define LCD_SHIFT_LEFT          0x00  /**< Shift/move left */

/* Commonly used composite values */
#define LCD_CMD_CURSOR_LEFT         0x10  /**< Move cursor one position left */
#define LCD_CMD_CURSOR_RIGHT        0x14  /**< Move cursor one position right */
#define LCD_CMD_DISPLAY_LEFT        0x18  /**< Shift entire display one position left */
#define LCD_CMD_DISPLAY_RIGHT       0x1C  /**< Shift entire display one position right */

/* --- Function Set (0x20–0x3F) ----------------------------------------------- */
/** @defgroup LCD_FUNC Function Set (0x20 base) */
#define LCD_CMD_FUNCTION_BASE   0x20  /**< Base opcode; OR the flags below */
#define LCD_FUNC_8BIT           0x10  /**< 8-bit data bus (DL=1) */
#define LCD_FUNC_4BIT           0x00  /**< 4-bit data bus (DL=0) */
#define LCD_FUNC_2LINE          0x08  /**< 2-line (or 4-line) display (N=1) */
#define LCD_FUNC_1LINE          0x00  /**< 1-line display (N=0) */
#define LCD_FUNC_FONT5x10       0x04  /**< 5×10 dot font (F=1, 1-line mode only) */
#define LCD_FUNC_FONT5x8        0x00  /**< 5×8 dot font  (F=0) */

/* Commonly used composite values */
#define LCD_CMD_FUNC_8BIT_1LINE_5x8  0x30  /**< 8-bit bus, 1-line, 5×8 font */
#define LCD_CMD_FUNC_8BIT_2LINE_5x8  0x38  /**< 8-bit bus, 2/4-line, 5×8 font (standard init) */
#define LCD_CMD_FUNC_4BIT_1LINE_5x8  0x20  /**< 4-bit bus, 1-line, 5×8 font */
#define LCD_CMD_FUNC_4BIT_2LINE_5x8  0x28  /**< 4-bit bus, 2/4-line, 5×8 font */

/* --- Set CGRAM Address (0x40–0x7F) ------------------------------------------ */
/**
 * Set CGRAM address counter.  After this command, subsequent LCD_DATA
 * writes store character generator pattern bytes at successive CGRAM
 * addresses.  The HD44780 supports 8 user-defined 5×8 characters (slots 0–7).
 *
 *   opcode = LCD_CMD_SET_CGRAM_ADDR | (char_index << 3) | row_index
 *   char_index: 0–7 (which of the 8 custom characters)
 *   row_index:  0–7 (which of the 8 pixel rows of that character)
 *
 *   Full CGRAM address = (char_index * 8) + row_index
 */
#define LCD_CMD_SET_CGRAM_BASE  0x40  /**< Base opcode; OR 6-bit CGRAM address */
#define LCD_CGRAM_ADDR(n)       (LCD_CMD_SET_CGRAM_BASE | ((n) & 0x3F))
#define LCD_CGRAM_CHAR(ch, row) LCD_CGRAM_ADDR(((ch) & 0x07) << 3 | ((row) & 0x07))

/* --- Set DDRAM Address (0x80–0xFF) ------------------------------------------ */
/**
 * Set DDRAM address counter.  After this command, subsequent LCD_DATA
 * writes go to the display at the addressed position.
 *
 * DMP9 / DMP16 display — 4-line × 20-char, using standard HD44780 mapping:
 *
 *   Line 1:  DDRAM 0x00–0x13   opcode 0x80–0x93
 *   Line 2:  DDRAM 0x40–0x53   opcode 0xC0–0xD3
 *   Line 3:  DDRAM 0x14–0x27   opcode 0x94–0xA7  (verify from firmware trace)
 *   Line 4:  DDRAM 0x54–0x67   opcode 0xD4–0xE7  (verify from firmware trace)
 *
 * The line 3/4 base offsets (0x14/0x54) are the most common variant for
 * 4-line 20-char displays but some modules use 0x10/0x50 or 0x00/0x40
 * for lines 3/4.  Cross-check against the firmware initialisation sequence.
 */
#define LCD_CMD_SET_DDRAM_BASE  0x80  /**< Base opcode; OR 7-bit DDRAM address */
#define LCD_DDRAM_ADDR(n)       (LCD_CMD_SET_DDRAM_BASE | ((n) & 0x7F))

/* DDRAM line base addresses (standard 4×20 mapping) */
#define LCD_DDRAM_LINE1         0x00  /**< DDRAM address for start of line 1 */
#define LCD_DDRAM_LINE2         0x40  /**< DDRAM address for start of line 2 */
#define LCD_DDRAM_LINE3         0x14  /**< DDRAM address for start of line 3 (verify) */
#define LCD_DDRAM_LINE4         0x54  /**< DDRAM address for start of line 4 (verify) */
#define LCD_DDRAM_LINE_LEN      20    /**< Characters per display line */

/* Convenience: opcode to jump to start of a numbered line (1-based) */
#define LCD_CMD_HOME_LINE1      LCD_DDRAM_ADDR(LCD_DDRAM_LINE1)  /* 0x80 */
#define LCD_CMD_HOME_LINE2      LCD_DDRAM_ADDR(LCD_DDRAM_LINE2)  /* 0xC0 */
#define LCD_CMD_HOME_LINE3      LCD_DDRAM_ADDR(LCD_DDRAM_LINE3)  /* 0x94 */
#define LCD_CMD_HOME_LINE4      LCD_DDRAM_ADDR(LCD_DDRAM_LINE4)  /* 0xD4 */

/* --- Busy Flag / Address Counter read (LCD_CMD read) ------------------------- */
/**
 * Reading LCD_CMD (RS=0) returns:
 *   Bit [7]: BF – Busy Flag (1=busy executing previous command)
 *   Bits[6:0]: AC – current Address Counter value (DDRAM or CGRAM)
 * Poll BF before each command write.  The DMP9 firmware may use fixed
 * delays instead of polling BF — check the firmware's read-back loop.
 */
#define LCD_BF                  0x80  /**< Busy Flag mask in command read */
#define LCD_AC_MASK             0x7F  /**< Address Counter mask in command read */


/* -------------------------------------------------------------------------
 * HD44780 Command → Semantic Decode Table
 *
 * Used by DMP9_Board_Setup.java to add descriptive pre-comments at every
 * store instruction that writes a constant byte to LCD_CMD.
 *
 * The decode logic in the Ghidra script applies the following rules in
 * order (most specific mask checked first):
 *
 *   Value range 0x80–0xFF  →  "Set DDRAM addr=0x%02X  [line %d, col %d]"
 *   Value range 0x40–0x7F  →  "Set CGRAM addr=0x%02X  [char %d, row %d]"
 *   Value        0x38       →  "Function Set: 8-bit, 2-line, 5×8  (standard init)"
 *   Value        0x30       →  "Function Set: 8-bit, 1-line, 5×8"
 *   Value        0x28       →  "Function Set: 4-bit, 2-line, 5×8"
 *   Value        0x20       →  "Function Set: 4-bit, 1-line, 5×8"
 *   Value        0x1C       →  "Shift Display Right"
 *   Value        0x18       →  "Shift Display Left"
 *   Value        0x14       →  "Shift Cursor Right"
 *   Value        0x10       →  "Shift Cursor Left"
 *   Value        0x0F       →  "Display ON + Cursor ON + Blink"
 *   Value        0x0E       →  "Display ON + Cursor ON"
 *   Value        0x0C       →  "Display ON, Cursor OFF"
 *   Value        0x08       →  "Display OFF"
 *   Value        0x07       →  "Entry Mode: Increment + Display Shift"
 *   Value        0x06       →  "Entry Mode: Increment, no shift"
 *   Value        0x05       →  "Entry Mode: Decrement + Display Shift"
 *   Value        0x04       →  "Entry Mode: Decrement, no shift"
 *   Value        0x03       →  "Return Home  (wait ≥1.52 ms)"
 *   Value        0x02       →  "Return Home  (wait ≥1.52 ms)"
 *   Value        0x01       →  "Clear Display  (wait ≥1.52 ms)"
 *   Value        0x00       →  "NOP / no-op write"
 * -------------------------------------------------------------------------- */


/* =========================================================================
 * SECTION 2 – YM-series DSP CHIPS
 *
 * The DMP9/DMP16 contains two independent DSP effect blocks, each
 * addressed via a dedicated base address.  Both blocks are accessed
 * using byte writes from the TMP68301; address decoding is external.
 *
 * EF1 block (effects 1):  YM3804 + YM6007 + YM3807 + YM6104
 * EF2 block (effects 2):  YM3804 + YM3807
 *
 * WARNING: Yamaha YM3804, YM3807, YM6007, and YM6104 are proprietary
 * DSP chips with no publicly available register documentation.  All
 * register/port offsets below are derived from firmware disassembly and
 * should be treated as tentative until verified.
 *
 * Ghidra annotation strategy (see DMP9_Board_Setup.java):
 *   - Label each DSP base as DSP_EF1_BASE / DSP_EF2_BASE
 *   - Mark the entire 64-KB window as an overlay block
 *   - Add a plate comment listing chip composition
 *   - As register offsets are discovered via firmware analysis,
 *     they can be added as further defineReg() calls below the base
 * ========================================================================= */

/* DSP block base addresses */
#define DSP_EF1_BASE            0x460000UL  /**< EF1: YM3804 + YM6007 + YM3807 + YM6104 */
#define DSP_EF2_BASE            0x470000UL  /**< EF2: YM3804 + YM3807 */

/* DSP block window size (address space reserved per block) */
