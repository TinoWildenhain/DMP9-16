/* =========================================================================
 * SECTION 3 – MEMORY
 *
 * Shared DRAM (IC35/IC36) at 0x400000 — CONFIRMED dual-purpose:
 *
 *   CPU side:  The ROM reset vector initialises the TMP68301 stack pointer
 *              just above 0x400000 (observed directly from firmware binary).
 *              This proves the CPU has read/write access to this region.
 *              The CPU uses the upper portion of this 64 KB window as its
 *              stack and working variable area.
 *
 *   DSP side:  The service manual block diagram shows IC35/36 feeding the
 *              DSP SECTION, specifically DSP2 instances 3, 4, and 5 (the
 *              main mix/master/send DSP units).  These units use the DRAM
 *              for audio delay lines and effects buffers.
 *
 *   Shared bus: A time-division or arbitrated bus allows both the TMP68301
 *              and the DSP to access the same physical DRAM.  This is a
 *              standard Yamaha DSP board design pattern.
 *
 * Probable memory layout within 0x400000–0x40FFFF:
 *   0x400000 – 0x40xxxx  DSP delay/effects buffer (lower region, DSP-written)
 *   0x40xxxx – 0x40FFFF  CPU stack and working variables (SP near top)
 *   Exact boundary TBD from firmware (watch for first CPU store instructions).
 *
 * IC93/IC94 (CPU section SRAM visible in block diagram):
 *   May be a separate static RAM at an unknown address used for patch/preset
 *   storage or display data — OR it may be the same ICs as IC35/36 seen
 *   from the CPU bus side.  Determine from PCB trace analysis or firmware.
 * ========================================================================= */

#define DMP9_SHARED_DRAM_BASE   0x400000UL  /**< Shared DRAM (IC35/36), 64 KB — CPU+DSP dual use */
#define DMP9_SHARED_DRAM_SIZE   0x010000UL  /**< DRAM size: 64 KB */
#define DMP9_SHARED_DRAM_END    (DMP9_SHARED_DRAM_BASE + DMP9_SHARED_DRAM_SIZE - 1)

/**
 * CPU stack top: SP is initialised just above 0x400000 at reset.
 * Actual value observed from ROM = 0x400000 + DRAM_SIZE = 0x410000, or
 * one of the values just below — read the exact SP init value from the
 * reset prologue (instruction at ROM+4 or first MOVE.L #imm, SP).
 */
#define DMP9_SP_INIT_REGION     DMP9_SHARED_DRAM_BASE  /**< SP initialised near top of this block */

/* Legacy aliases */
#define DMP9_DSP_DRAM_BASE      DMP9_SHARED_DRAM_BASE
#define DMP9_DSP_DRAM_SIZE      DMP9_SHARED_DRAM_SIZE
#define DMP9_DRAM_BASE          DMP9_SHARED_DRAM_BASE
#define DMP9_DRAM_SIZE          DMP9_SHARED_DRAM_SIZE


