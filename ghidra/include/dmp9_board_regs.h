/**
 * @file dmp9_board_regs.h
 * @brief Yamaha DMP9 / DMP16 Board-Specific Peripheral Definitions
 *
 * This header is a companion to tmp68301_regs.h and provides address
 * definitions, constants, and bitfield descriptions for the external
 * peripherals found on the Yamaha DMP9 and DMP16 digital rack mixer
 * boards.
 *
 * ===========================================================================
 * BOARD ARCHITECTURE SUMMARY (from Yamaha DMP9/DMP16 service manual diagrams)
 * ===========================================================================
 *
 * CPU Section (DIG board, IC24):
 *   IC24   = TMP68301A @ 16 MHz  — main CPU
 *   IC34   = ROM                 — firmware (single ROM chip)
 *   IC93/94= SRAM                — CPU working SRAM (address TBD from firmware)
 *
 * DSP Section (shaded "DSP SECTION" block in block diagrams):
 *   DEQ2 units: IC26,27,28,37,38,39  (6 units, labelled DEQ2-1..6, one per
 *               stereo input pair; "DEQ2" = waveform format name per diagram
 *               legend; likely implemented by YM3807)
 *   DSP2 units: IC19,29,46,56,70,71  (5 units, labelled DSP2-1..5 + master;
 *               handle Mix/Level/On-Off per group; likely YM3804)
 *   MEG unit:   IC22,23              (metering engine; AUXMEL0/1, IMEL0-3,
 *               OMEL0/1 signals; likely YM6104 or YM6007)
 *   SRAM:       IC66,67,68           (DSP coefficient/program SRAM in DSP block)
 *   DRAM:       IC35,36              (64 KB delay/effects DRAM used by DSP2
 *                                    units 3/4/5 — see note below)
 *
 * IMPORTANT: The DRAM at 0x400000 is DUAL-PURPOSE (CPU + DSP shared).
 *   The reset vector places the TMP68301 stack pointer just above 0x400000
 *   (confirmed from ROM), proving the CPU has full read/write access to this
 *   region.  The DSP2 section also uses the same physical ICs (IC35/36) for
 *   delay-based effects via a time-division shared bus — a standard arrangement
 *   in late-80s/early-90s Yamaha DSP hardware.
 *   Probable layout within 0x400000–0x40FFFF:
 *     Low addresses  : DSP delay/effects buffer (written by DSP2 units 3/4/5)
 *     High addresses : CPU stack and working variables (SP init near 0x40FFFF)
 *   The exact boundary between DSP and CPU usage must be confirmed from
 *   firmware analysis (watch for the SP initialisation and first MOVE.L
 *   writes into the region).
 *
 *   The IC93/94 SRAM visible in the CPU section block is likely a separate,
 *   smaller static RAM used for a different purpose (e.g. patch/preset storage
 *   or display buffer) at an as-yet-unknown address — or it may be the same
 *   physical chips as IC35/36 seen from the CPU bus side (board-dependent).
 *
 * Clock / PLL Section:
 *   IC45/IC49/IC96 + XTAL  — PLL generating 512FS @ 48 kHz
 *   XTAL frequency = 512 × 48000 = 24.576 MHz
 *   IC50 + IC30,31,51,57   — DIV & PLD: clock division and address decode logic
 *   Outputs: 256(M), 128(M), 64(M), FS(M), SYNC to DSP section
 *            256(D1/D2), 64(D1/D2), FS(D1/D2) to digital input receivers
 *
 * Address Decoding:
 *   Performed entirely by IC50 (PLD) and supporting divider ICs.
 *   The TMP68301 CS0/CS1 chip-select outputs and AMAR0/AAMR0/AMAR1/AAMR1
 *   registers do NOT govern the LCD or DSP address windows.
 *   CS CONTROL signals (LOCK(D1), LOCK(D2), LOCK(M)) are generated by the
 *   PLD based on clock-lock status of the digital input receivers.
 *
 * Digital I/O:
 *   IC58,101 = DIR2  — two digital input receivers (AES/EBU or S/PDIF)
 *   IC43,100 = DIT2  — two digital output transmitters
 *   IC73,65,59       — WAVE REFORM for digital input 1
 *
 * Signal format legend (from diagram footnote):
 *   Labels in <> (e.g. <ST-DSP>, <DSP2>) = waveform/data format names
 *   Labels without <> (e.g. DEQ2, DSP2, MEG) = functional block names
 *   Both are used on the SIO bus signals connecting DSP ICs.
 *
 * Analog path (ANAU/ANAL/ANA boards):
 *   Per-input chain: Op-Amp → FPB → Pre-Emp → DC OFFSET → A/D Converter
 *   Output chain:    D/A → Op-Amp → De-Emp → FPR → balanced/unbalanced out
 *   CLK(FOR MASTER) and CLK(FOR MON) are separate clock domains.
 *
 * LCD physical interface:
 *   PWR board CN5 (PH-2P TE) carries LCD backlight power (separate from data).
 *   Data interface (LCD_CMD/LCD_DATA) is on the DIG board, routed via CPU.
 *
 * ===========================================================================
 * CONFIRMED MEMORY MAP (DMP9/DMP16)
 * ===========================================================================
 *   0x000000           ROM           (IC34 — single firmware ROM)
 *   0x400000           SHARED_DRAM   (IC35,36 — 64 KB, dual-purpose)
 *                                   CPU: stack + working RAM  (SP init just above 0x400000,
 *                                        confirmed from ROM reset vector)
 *                                   DSP: delay/effects buffer (DSP2 units 3/4/5)
 *   0x460000           DSP_EF1_BASE  (YM3804 + YM6007 + YM3807 + YM6104)
 *   0x470000           DSP_EF2_BASE  (YM3804 + YM3807)
 *   0x490000           LCD_CMD       (HD44780, RS=0 — command register)
 *   0x490001           LCD_DATA      (HD44780, RS=1 — data register)
 *   ???                CPU_SRAM      (IC93,IC94 — address TBD)
 *   0xFFFC00           TMP68301 internal peripheral registers
 *
 * Sources:
 *   - Yamaha DMP9/DMP16 service manual hardware block diagrams (N8-B7)
 *   - Yamaha DMP9/DMP16 hardware analysis (firmware disassembly)
 *   - HD44780U datasheet, Hitachi Semiconductor, rev. 0.0
 *   - Yamaha DSP chip list (dtech.lv/techarticles_yamaha_chips.html)
 */

